function custom_login() {
    if ( empty( $_POST['username'] ) || empty( $_POST['password'] ) ) {
        wp_redirect( home_url( '/login/?login=emptyxxx' ) );
        exit;
    }

    $username = sanitize_text_field( $_POST['username'] );
    $password = sanitize_text_field( $_POST['password'] );

    global $wpdb;
    $table_name = $wpdb->prefix . 'tb_login';

    $query = $wpdb->prepare( "SELECT * FROM $table_name WHERE username = %s", $username );
    $user = $wpdb->get_row( $query );

    // if ( ! $user || ! wp_check_password( $password, $user->password ) ) {
    //     wp_redirect( home_url( '/login/?login=failed' ) );
    //     exit;
    // }
    if ( ! $user || ! ($password === $user->password)  ) {
			wp_redirect( home_url( '/login/?login=failed' ) );
			exit;
	}
    session_start();

    $_SESSION['user_id'] = $user->id;
    $_SESSION['user_username'] = $user->username;

    setcookie( 'custom_login_session', session_id(), time() + 60 * 60 * 24 * 7, '/' );

    wp_redirect( home_url( '/profile' ) );
    exit;
}



